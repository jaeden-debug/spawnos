export default function MobileNav() {
  const pathname = usePathname()

  function isActive(item: { href: string; exact?: boolean }) {
    if (item.exact) return pathname === item.href
    return pathname.startsWith(item.href)
  }

  return (
    <nav className="lg:hidden fixed bottom-0 left-0 right-0 z-50 bg-spawn-surface border-t border-spawn-border safe-area-bottom">
      <div className="flex items-center justify-around px-2 py-2">
        {MOBILE_NAV.map((item) => (
          <Link
            key={item.href}
            href={item.href}
            className={cn(
              'flex flex-col items-center gap-1 px-3 py-2 rounded-xl transition-all min-w-[56px]',
              isActive(item)
                ? 'text-spawn-cyan'
                : 'text-spawn-muted hover:text-spawn-muted-text'
            )}
          >
            {item.icon}
            <span className="text-[10px] font-medium">{item.label}</span>
          </Link>
        ))}
      </div>
    </nav>
  )
}
